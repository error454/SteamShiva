function Steam.onWorkshopItemSubscribed ( sItemID )
--------------------------------------------------------------------------------
	
	log.message ( this.sDebugLabel ( ), "User subscribed to item: ", sItemID )
	this.downloadWorkshopItem ( sItemID )
    
--------------------------------------------------------------------------------
end
