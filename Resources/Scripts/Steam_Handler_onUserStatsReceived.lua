--------------------------------------------------------------------------------
function Steam.onUserStatsReceived ( bOK )
--------------------------------------------------------------------------------
	
    log.message ( this.sDebugLabel ( ), "User stats were received" )
    if bOK then
        if this.bDebug ( ) then
            this.debugPrintStats ( )
        end
    end
	
--------------------------------------------------------------------------------
end
