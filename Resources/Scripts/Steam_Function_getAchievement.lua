function Steam.getAchievement ( sAchievement )
--------------------------------------------------------------------------------
	
	if user.getAIState ( application.getCurrentUser ( ), "Steam" ) == "Idle" then 
        return false 
    end
    
    return Steamworks.GetAchievement ( sAchievement )
	
--------------------------------------------------------------------------------
end
