function Steam.achievementsInit ( )
--------------------------------------------------------------------------------
	
	--
	-- Initialize your game's achievements here
	--
    local t = this.tAchievements ( )
    local tAdd = table.add
    tAdd ( t, "ACH_WIN_ONE_GAME" )
    tAdd ( t, "ACH_WIN_100_GAMES" )
    tAdd ( t, "ACH_TRAVEL_FAR_ACCUM" )
    tAdd ( t, "ACH_TRAVEL_FAR_SINGLE" )
    tAdd ( t, "NEW_ACHIEVEMENT_0_4" )
	
--------------------------------------------------------------------------------
end
