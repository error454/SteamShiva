function Steam.onPostInit (  )
--------------------------------------------------------------------------------
	
	this.achievementsInit ( )
    this.statsInit ( )
	local bSuccess = Steamworks.Init ( "Steam" )

    if bSuccess then
        if this.bDebug ( ) then
            this.Debug ( )
        else
            this.Running ( )
        end
    end
	
--------------------------------------------------------------------------------
end
