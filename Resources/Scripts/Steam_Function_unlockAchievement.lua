function Steam.unlockAchievement ( sAchievementID )
--------------------------------------------------------------------------------
	
	if user.getAIState ( this.getUser ( ), "Steam" ) == "Idle" then return 0 end
    
    Steamworks.SetAchievement ( sAchievementID )
	
--------------------------------------------------------------------------------
end
